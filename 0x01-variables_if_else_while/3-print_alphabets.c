#include <stdio.h>

/**
 * main - Prints the alphabet in both lowercase and  uppercase respectively
 * Return is 0 always
 */
int main(void)
{
	char letter;

	for (letter = 'a'; letter <= 'z'; letter++)
		putchar(letter);

	for (letter = 'A'; letter <= 'Z'; letter++)
		putchar(letter);

	putchar('\n');

	return (0);
}
